package com.pvpdojo;

public class CombatantSkills
{
    public final int ATTACK;
    public final int STRENGTH;
    public final int DEFENCE;
    public final int MAGIC;
    public final int RANGE;

    public CombatantSkills(int ATTACK, int STRENGTH, int DEFENCE, int MAGIC, int RANGE)
    {
        this.ATTACK = ATTACK;
        this.STRENGTH = STRENGTH;
        this.DEFENCE = DEFENCE;
        this.MAGIC = MAGIC;
        this.RANGE = RANGE;
    }
}
